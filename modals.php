                <h3><b>DATA PRIVACY COMPLIANCE POLICY STATEMENT</b></h3>
                <h3><b>Lendell Outsourcing Solutions, Inc. Updated 2022</b></h3>
                <br>
                <h4><b>INTRODUCTION</b></h4>
                <p>At LENDELL, your privacy is as important to us as it is to you. We have created this policy
                because we want you to feel confident about the privacy and security of your personal data.
                We are committed to protecting the privacy of individuals that enter contracts with LENDELL.
                This Privacy Policy is without prejudice to specific local and or international data protection
                requirements and additional local privacy notices that may be posted, also on our websites,
                that may be included in our Contracts or that may be notified to the customer from time to
                time in relation to our Services.
                Lendell Outsourcing Solutions, Inc. respects and is committed to maintaining the privacy of all
                individuals who provide personal information to us. LENDELL’S Privacy Policy governs how to
                deal with the collection, security, quality, use and disclosure of personal information in
                compliance with the Data Privacy Act of 2012 or the Republic Act No. 10173.</p>
                <h5><b>1. Data protection principles</b></h5>
                <p>The Company is committed to processing data in accordance with its responsibilities under
                the DATA PRIVACY ACT OF 2012.
                Per DATA PRIVACY ACT OF 2012, requires that personal data shall be:
                a. processed lawfully, fairly and in a transparent manner in relation to individuals;
                b. collected for specified, explicit and legitimate purposes and not further processed in a
                manner that is incompatible with those purposes; further processing for archiving
                purposes in the public interest, scientific or historical research purposes or statistical
                purposes shall not be considered to be incompatible with the initial purposes;
                c. adequate, relevant and limited to what is necessary in relation to the purposes for which
                they are processed;
                d. accurate and, where necessary, kept up to date; every reasonable step must be taken
                to ensure that personal data that are inaccurate, having regard to the purposes for
                which they are processed, are erased or rectified without delay; e. kept in a form which permits identification of data subjects for no longer than is
                necessary for the purposes for which the personal data are processed; personal data
                may be stored for longer periods insofar as the personal data will be processed solely
                for archiving purposes in the public interest, scientific or historical research purposes or
                statistical purposes subject to implementation of the appropriate technical and
                organisational measures required by the DATA PRIVACY ACT OF 2012 in order to
                safeguard the rights and freedoms of individuals; and
                f. processed in a manner that ensures appropriate security of the personal data, including
                protection against unauthorised or unlawful processing and against accidental loss,
                destruction or damage, using appropriate technical or organisational measures.”</p>
                <br>
                <h5><b>2. General provisions</b></h5>
                <p>a. This policy applies to all personal data processed by the Company.<br>
                b. The Responsible Person shall take responsibility for the Company’s ongoing compliance
                with this policy.<br>
                c. This policy shall be reviewed at least annually.<br>
                d. The Company shall register with the Information Commissioner’s Office as an
                organisation that processes personal data.</p><br>
                <h5><b>3. Lawful, fair and transparent processing</b></h5>
                <p>a. To ensure its processing of data is lawful, fair and transparent, the Company shall
                maintain a Register of Systems.<br>
                b. The Register of Systems shall be reviewed at least annually.<br>
                c. Individuals have the right to access their personal data and any such requests made to
                the Company shall be dealt with in a timely manner.<br></p>
                <h5><b>4. Lawful purposes</b></h5>
                <p>a. All data processed by the Company must be done on one of the following lawful bases:
                consent, contract, legal obligation, vital interests, public task or legitimate interests.<br>
                b. The Company shall note the appropriate lawful basis in the Register of Systems.<br>
                c. Where consent is relied upon as a lawful basis for processing data, evidence of opt-in
                consent shall be kept with the personal data.<br>
                d. Where communications are sent to individuals based on their consent, the option for
                the individual to revoke their consent should be clearly available and systems should
                be in place to ensure such revocation is reflected accurately in the Company’s systems.</p><br>
                <h5><b>5. Data minimisation</b></h5>
                <p>a. The Company shall ensure that personal data are adequate, relevant and limited to
                    what is necessary in relation to the purposes for which they are processed.  </p><br>
                <h5><b>6. Accuracy</b></h5>
                <p>a. The Company shall take reasonable steps to ensure personal data is accurate.<br>
                   b. Where necessary for the lawful basis on which data is processed, steps shall be put in
                   place to ensure that personal data is kept up to date. </p><br>
                <h5><b>7. Archiving / removal</b></h5>
                <p>a. To ensure that personal data is kept for no longer than necessary, the Company shall
                    put in place an archiving policy for each area in which personal data is processed and
                    review this process annually.<br>
                    b. The archiving policy shall consider what data should/must be retained, for how long,
                    and why.</p><br>
                <h5><b>8. Security</b></h5>
                <p>
                    a. The Company shall ensure that personal data is stored securely using modern software
                    that is kept-up-to-date.<br>
                    b. Access to personal data shall be limited to personnel who need access and
                    appropriate security should be in place to avoid unauthorised sharing of information.<br>
                    c. When personal data is deleted this should be done safely such that the data is
                    irrecoverable.<br>
                    d. Appropriate back-up and disaster recovery solutions shall be in place
                </p><br>
                <h5><b>9. Breach</b></h5>
                <p>
                    In the event of a breach of security leading to the accidental or unlawful destruction,
                    loss, alteration, unauthorised disclosure of, or access to, personal data, the Company shall
                    promptly assess the risk to people’s rights and freedoms and if appropriate report this breach
                    in accordance to the governing laws.
                </p><br>
                <h5><b>10. CONSENT</b></h5>
                <p>
                    As stated above some of our processing activities will be based on your consent. In such case,
                    you will have the right to withdraw your consent at any time. If you withdraw your consent, we
                    will cease to process your personal data, unless and to the extent the continued processing or
                    storage is permitted or required according to the applicable personal data legislation or other
                    applicable laws and regulations. Please note that the withdrawal of your consent will not
                    affect the lawfulness of processing conducted prior to the withdrawal. Further, because of
                    your withdrawal of your consent, we may not be able to satisfy your requests or provide you
                    with our services.
                </p><br>
                <h5><b>11. RIGHTS</b></h5>
                <p>
                    We reserve the right to review this Privacy Policy at any time. Where appropriate or required
                    by law, we will notify you about the new privacy Policy by posting appropriate notice on our
                    Services or Website or by e-mail (to the extent that we have your e-mail address). We
                    encourage you to periodically review this Privacy Policy to stay informed about our information
                    practices. We will indicate above the date on which this Privacy Policy was last modified.
                </p><br>
                <h5><b>12. COMPLAINTS</b></h5>
                <p>
                    If you have any complaints about the processing of personal data carried out by LENDELL,
                    please feel free to contact us at any time. We will review and assess your complaint and if
                    necessary, we may contact you to obtain further information. We will always acknowledge
                    receipt of your complaint or objection within 5 business days. We strive at processing any
                    complaint or objection within one month. If it is not possible to decide within one month, we
                    will inform you of the grounds for the delay and of the time at which the decision can be
                    expected to be provided.
                </p>

                  
                   
